namespace ExcelBot.Util
{
    /// <summary>
    /// Bot configuration information.
    /// </summary>
    public struct BotInfo
    {
        /// <summary>
        /// Gets or sets the application Urn.
        /// </summary>
        /// <value>The application Urn.</value>
        public string ApplicationUrn { get; set; }

        /// <summary>
        /// Gets or sets the application user agent.
        /// </summary>
        /// <value>The application user agent.</value>
        public string ApplicationUserAgent { get; set; }

    }
}
